DROP DATABASE IF EXISTS aeropuertos;

CREATE DATABASE IF NOT EXISTS aeropuertos;

USE aeropuertos;

-- CREACIÓN DE TABLAS

-- DROP TABLE ciudad;
CREATE TABLE ciudad (
    id_ciudad INT PRIMARY KEY AUTO_INCREMENT,
    nombre VARCHAR(100) NOT NULL,
    pais VARCHAR(100) NOT NULL,
    poblacion INT NOT NULL,
    zonaHoraria VARCHAR(100) NOT NULL,
    latitud DECIMAL NOT NULL,
    longitud DECIMAL NOT NULL,
    elevacion INT NOT NULL,
    anyoFundacion INT NOT NULL
);

-- DROP TABLE aeropuerto;
CREATE TABLE aeropuertos (
    id_aeropuerto INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL,
    iata VARCHAR(100) NOT NULL,
    ciudadId INT NOT NULL,
    tipo ENUM("Internacional","Domestico","Regional") NOT NULL,
    latitud DECIMAL NOT NULL,
    longitud DECIMAL NOT NULL,
    elevacion INT NOT NULL,
    terminales INT NOT NULL,
    anyoApertura INT NOT NULL,

    FOREIGN KEY (ciudadId) REFERENCES ciudad(id_ciudad)
);

